package basic;

public class Switch {
	public static void main(String[]args) {
		int g ='5';
		switch(g) {
		case'1':
			System.out.println("speed of car is 10kmph");
			break;
		case'2':
			System.out.println("speed of car is 40kmph");
			break;
		case'3':
			System.out.println("speed of car is 100kmph");
			break;
		case'4':
			System.out.println("speed of car is 150kmph");
			break;
		default :
			System.out.println("neutral");
		
		
		
		}
		
	}


}
